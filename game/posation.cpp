#include "Posation.h"
#include <iostream>
#include <string>
using namespace std ;
//**********************************************************************************************************************************************************
Posation::Posation( int row , int col )
{
    this->row = row ;
    this->col = col ;
}
//**********************************************************************************************************************************************************
Posation::~Posation()
{
    //dtor
}
//**********************************************************************************************************************************************************
int Posation::getRow()
{
    return row ;
}
//**********************************************************************************************************************************************************
int Posation::getCol()
{
    return col ;
}
//**********************************************************************************************************************************************************
bool Posation::isEqual( int row , int col )
{
    return this->row == row && this->col == col ;
}
//**********************************************************************************************************************************************************
bool Posation::isEqual( Posation &p )
{
    return row == p.row && col == p.col ;
}
//**********************************************************************************************************************************************************
void Posation::setRow( int row )
{
    this->row = row ;
}
//**********************************************************************************************************************************************************
void Posation::setCol( int col )
{
    this->col = col ;
}
//**********************************************************************************************************************************************************
